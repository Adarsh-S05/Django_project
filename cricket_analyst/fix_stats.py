for name, data in REAL_DATA.items():
    (pid, matches, runs, wickets, bat_avg, sr, econ,
     hs, best, hundreds, fifties) = data

    bowling_avg = round(runs / max(wickets, 1), 1) if wickets > 0 else 0.0

    rows = Player.objects.filter(name=name)
    if rows.exists():
        rows.update(
            photo_url      = make_photo_url(pid),
            total_matches  = matches,
            total_runs     = runs,
            total_wickets  = wickets,
            batting_avg    = bat_avg,
            strike_rate    = sr,
            economy_rate   = econ,
            bowling_avg    = bowling_avg,
            highest_score  = hs,
            best_bowling   = best,
            centuries      = hundreds,
            half_centuries = fifties,
        )
        updated += rows.count()
    else:
        not_found.append(name)

print(f"\n✅ Updated stats for {updated} players.")
if not_found:
    print(f"⚠️  Not found in DB ({len(not_found)}): {', '.join(not_found[:10])}")
print("\n🏏 Restart server: python manage.py runserver")